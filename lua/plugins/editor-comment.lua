--
-- 설명:
--   단축키 하나로 코드 주석을 토글하는 플러그인.
--   treesitter를 활용해 언어별 주석 문법을 자동으로 감지한다.
--   임베디드 언어(HTML 안의 JS 등)도 올바른 주석 문법을 사용한다.
--
--   언어별 주석 문법:
--   Go         - // (줄 주석), /* */ (블록 주석)
--   Python     - # (줄 주석)
--   SQL        - -- (줄 주석)
--   YAML       - # (줄 주석)
--   Bash       - # (줄 주석)
--   Dockerfile - # (줄 주석)
--
-- 사용법:
--   Normal 모드:
--   gcc        - 현재 줄 주석 토글
--   gbc        - 현재 줄 블록 주석 토글
--   gcj        - 현재 줄 + 아래 줄 주석 토글
--   gcip       - 현재 단락(paragraph) 주석 토글
--   gc3j       - 현재 줄 포함 아래 3줄 주석 토글
--
--   Visual 모드:
--   gc         - 선택 영역 줄 주석 토글
--   gb         - 선택 영역 블록 주석 토글
--
-- 커스텀 단축키:
--   없음
-- ============================================================================

