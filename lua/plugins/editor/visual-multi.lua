--
-- 설명:
--   VSCode 스타일의 멀티커서 편집 플러그인.
--   임의의 여러 위치에 동시에 커서를 배치하고 동시 편집이 가능하다.
--   단어 선택, 정규식 매칭, 수동 커서 추가 등 다양한 방식으로 커서를 생성한다.
--   Vimscript 기반으로 Vim 8과 Neovim 모두 지원한다.
--
-- 활용 예시:
--   - 같은 변수명을 여러 곳에서 동시에 변경
--   - 여러 줄의 특정 위치에 커서를 놓고 동시에 텍스트 입력
--   - 정규식으로 매칭되는 모든 위치에 커서를 놓고 일괄 편집
--   - 여러 줄의 줄 끝에 동시에 텍스트 추가
--
-- 사용법:
--   1. 단어 선택 방식:
--      - <C-n>으로 현재 커서 아래 단어 선택
--      - <C-n> 반복으로 다음 매칭 단어에 커서 추가
--      - q로 현재 매칭 건너뛰고 다음으로 이동
--      - Q로 현재 커서 제거
--
--   2. 수동 커서 추가 방식:
--      - <C-Down>/<C-Up>으로 아래/위에 커서 추가
--      - \\gS로 정규식 매칭 위치에 커서 생성
--
--   3. 전체 선택 방식:
--      - \\A로 현재 단어의 모든 매칭에 커서 생성
--      - \\/로 정규식 검색 후 모든 매칭에 커서 배치
--
--   4. 모드 전환:
--      - <Tab>으로 커서 모드(Cursor) ↔ 확장 모드(Extend) 전환
--        - 커서 모드: 각 커서 위치에서 Normal 모드 명령 실행
--        - 확장 모드: 각 커서에서 Visual 선택 영역 확장
--
--   5. 편집:
--      - 멀티커서 상태에서 c, i, a, I, A 등으로 동시 편집
--      - n/N으로 다음/이전 매칭으로 이동
--      - <Esc>으로 멀티커서 모드 종료
--
-- 기본 단축키:
--   | 단축키         | 설명                                      |
--   |----------------|-------------------------------------------|
--   | <C-n>          | 현재 단어 선택 / 다음 매칭 추가 선택      |
--   | <C-Down>       | 아래로 커서 추가                           |
--   | <C-Up>         | 위로 커서 추가                             |
--   | \\A            | 현재 단어의 모든 매칭에 커서 생성          |
--   | \\/            | 정규식 검색 후 커서 배치                   |
--   | \\gS           | 정규식 매칭 위치에 커서 생성               |
--   | <Tab>          | 커서 모드 ↔ 확장 모드 전환                 |
--   | q              | 현재 매칭 건너뛰기                         |
--   | Q              | 현재 커서 제거                             |
--   | n / N          | 다음 / 이전 매칭으로 이동                  |
--   | <Esc>          | 멀티커서 모드 종료                         |
-- ============================================================================

return {
	"mg979/vim-visual-multi",
	branch = "master",
	event = { "BufReadPost", "BufNewFile" },
	init = function()
		-- 기본 매핑 활성화
		vim.g.VM_default_mappings = 1
		-- 멀티커서 모드에서 상태 메시지 표시
		vim.g.VM_show_warnings = 1
		-- 테마 설정 (Iceblue, Ocean, Sand, Purplegray 등)
		vim.g.VM_theme = "ocean"
	end,
}
