local opt = vim.opt

-- ============================================================================
-- 줄 번호
-- ============================================================================
opt.number = true -- 현재 줄의 절대 번호 표시
opt.relativenumber = true -- 현재 줄 기준 상대 번호 표시 (점프 거리 파악에 유용)

-- ============================================================================
-- 들여쓰기
-- ============================================================================
opt.expandtab = true -- Tab 키 입력 시 실제 탭 대신 스페이스로 변환
opt.tabstop = 4 -- 탭 문자를 스페이스 몇 칸으로 표시할지
opt.shiftwidth = 4 -- >>, << 등 들여쓰기 시 사용할 스페이스 수
opt.softtabstop = 4 -- 편집 중 Tab/Backspace 키로 이동하는 스페이스 수
opt.smartindent = true -- 새 줄에서 문맥에 맞게 자동 들여쓰기

-- ============================================================================
-- 검색
-- ============================================================================
opt.ignorecase = true -- 검색 시 대소문자 무시
opt.smartcase = true -- 대문자 포함 입력 시 대소문자 구분 (ignorecase 오버라이드)
opt.hlsearch = true -- 검색 결과 전체 하이라이팅
opt.incsearch = true -- 입력하는 동안 실시간으로 검색 결과 표시

-- ============================================================================
-- UI
-- ============================================================================
opt.termguicolors = true -- 터미널에서 24bit RGB 색상 활성화 (테마 정상 표시에 필요)
opt.cursorline = true -- 현재 커서가 위치한 줄 하이라이팅
opt.scrolloff = 8 -- 커서 위/아래로 항상 유지할 최소 줄 수
opt.sidescrolloff = 8 -- 커서 좌/우로 항상 유지할 최소 컬럼 수
opt.signcolumn = "yes" -- 좌측 sign 컬럼 항상 표시 (LSP 진단 아이콘 등)
opt.wrap = true -- 긴 줄 자동 줄바꿈 true(활성화), false(비활성화)

-- ============================================================================
-- 클립보드
-- ============================================================================
opt.clipboard = "unnamedplus" -- 시스템 클립보드와 neovim 레지스터 공유

-- ============================================================================
-- 창 분할
-- ============================================================================
opt.splitright = true -- 수직 분할(vsplit) 시 새 창을 오른쪽에 배치
opt.splitbelow = true -- 수평 분할(split) 시 새 창을 아래쪽에 배치

-- ============================================================================
-- 파일
-- ============================================================================
opt.undofile = true -- undo 이력을 파일로 저장 (재시작 후에도 undo 가능)
opt.swapfile = false -- swap 파일 생성 비활성화
opt.backup = false -- 백업 파일 생성 비활성화
opt.autoread = true -- 외부에서 파일이 변경되면 자동으로 다시 읽어옴

-- ============================================================================
-- 성능
-- ============================================================================
opt.updatetime = 250 -- CursorHold 이벤트 발생까지 대기 시간(ms) (LSP 진단 속도에 영향)
opt.timeoutlen = 300 -- 키 조합 대기 시간(ms) (which-key 팝업 등에 영향)

-- ============================================================================
-- 폴딩 (nvim-ufo 사용)
-- ============================================================================
opt.foldmethod = "expr" -- nvim-ufo가 expr 방식으로 동작
opt.foldexpr = "0" -- nvim-ufo가 직접 처리하므로 기본값 0으로 설정
opt.foldlevel = 99 -- 기본적으로 모두 펼친 상태로 시작
opt.foldlevelstart = 99 -- 파일 열 때 모두 펼친 상태로 시작
opt.foldenable = true -- nvim-ufo가 폴딩 관리

-- ============================================================================
-- 진단 (LSP diagnostic) 표시 설정
-- ============================================================================
vim.diagnostic.config({
	float = {
		border = "rounded", -- 팝업 테두리 스타일
		source = true, -- 진단 출처 표시 (어떤 LSP/린터인지)
		header = "", -- 팝업 상단 헤더 제거
		prefix = "", -- 항목 앞 접두사 제거
	},
	virtual_text = true, -- 코드 우측에 인라인 진단 메시지 표시
	signs = true, -- 좌측 gutter에 진단 아이콘 표시
	underline = true, -- 진단 위치 밑줄 표시
	update_in_insert = false, -- insert 모드 중에는 진단 업데이트 안 함
})

-- ============================================================================
-- 기타
-- ============================================================================
opt.mouse = "a" -- 모든 모드에서 마우스 사용 활성화
opt.encoding = "utf-8" -- neovim 내부 인코딩
opt.fileencoding = "utf-8" -- 파일 저장 시 인코딩
opt.showmode = false -- 하단 모드 표시 비활성화 (lualine이 대체)
opt.pumheight = 10 -- 자동완성 팝업 최대 표시 항목 수
opt.conceallevel = 0 -- 텍스트 숨김(conceal) 비활성화 (마크다운 등에서 원본 표시)
